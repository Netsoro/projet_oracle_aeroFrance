  CREATE OR REPLACE TRIGGER "PROJET_ORACLE_DIAROG"."DECL_INSERT_OCC_VOL" 
INSTEAD OF INSERT ON view_occurence_vol
REFERENCING OLD AS OLS NEW AS NEW
FOR EACH ROW
DECLARE
  vOCCNUM_BIDON OCCURENCE_VOL.OCCNUM%TYPE :=0;
  vPKNUM PARKING.PKNUM%TYPE := 0;
  vPORTENUM PORTE.PORTENUM%TYPE :=0;
  vPASSNUM  PASSERELLE.PASSNUM%TYPE :=0;
  vPASSCAPACITE PASSERELLE.PASSCAPACITE%TYPE;
  
BEGIN

  --création de l'occurence de vol
   INSERT INTO OCCURENCE_VOL(OCCNUM,VOLNUM,AERONUM,OCCDATE,OCCETAT) VALUES ( :NEW.OCCNUM,:NEW.VOLNUM,:NEW.AERONUM,:NEW.OCCDATE,'ouvert_reservation');

  --On recupere l'occurence Bidon du vol s'il existe
  SELECT OCCNUM 
  INTO vOCCNUM_BIDON
  FROM OCCURENCE_VOL occ_v
  WHERE occ_v.VOLNUM = :NEW.VOLNUM AND 
        occ_v.OCCDATE = to_date('1/1/00','DD/MM/YY');

  --On recupere le couple porte/parking de l'occurence bidon
  
  BEGIN
    SELECT ap.PORTENUM,apk.PKNUM
    INTO vPORTENUM,vPKNUM
    FROM AFFECTER_PORTE ap, AFFECTER_PK apk
    WHERE ap.OCCNUM = apk.OCCNUM AND 
          ap.OCCNUM = vOCCNUM_BIDON AND 
          ROWNUM = 1
          ORDER BY  ap.PORTENUM;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
        dbms_output.put_line('IL N''YA PAS DE COUPLE PORTE/PK POUR CETT OCCURENCE BIDON ' );  
    WHEN OTHERS THEN
        dbms_output.put_line('ERREUR  DAND LE BLOC RECUPERATION PORTE/PK  ==> ' || SQLERRM);  
  END;
  
    DBMS_OUTPUT.PUT_LINE('OCC BIDON  =====>> ' || vOCCNUM_BIDON);
    DBMS_OUTPUT.PUT_LINE('NOUVEL OCC =====>> ' ||   :NEW.OCCNUM);
    
  --Si l'occurence bidon à un couple porte/parking de dispo
  IF vPORTENUM <> 0 AND  vPKNUM <> 0 THEN 
     --On selectionne la passerelle associé au couple
     BEGIN
          SELECT pass.PASSNUM, pass.PASSCAPACITE
            INTO vPASSNUM,vPASSCAPACITE
            FROM PASSERELLE pass
            WHERE pass.PORTENUM = vPORTENUM AND 
                  pass.PKNUM = vPKNUM AND 
                  ROWNUM = 1 
            ORDER BY pass.PASSCAPACITE;
                    
          -- Si une passerelle est associé on affecte le triplet
          INSERT INTO AFFECTER_PORTE(OCCNUM,PORTENUM,AFFPTETAT) VALUES (:NEW.OCCNUM, vPORTENUM,'affecté');
          INSERT INTO AFFECTER_PK(OCCNUM,PKNUM,AFFPTKETAT) VALUES (:NEW.OCCNUM, vPKNUM,'affecté');  
          UPDATE PASSERELLE
            SET PORTENUM = vPORTENUM ,
                PKNUM = vPKNUM 
            WHERE  PASSNUM = vPASSNUM;  
          dbms_output.put_line('(PORTENUM, PKNUM ,PASSNUM )  ===>> ' || '('|| vPORTENUM||','||vPKNUM||','||vPASSNUM||')');

     EXCEPTION 
      WHEN NO_DATA_FOUND THEN
        -- Dans le cas ou il n'ya pas de passerelle de dispo on affecte quand même le couple porte/pk
        INSERT INTO AFFECTER_PORTE(OCCNUM,PORTENUM,AFFPTETAT) VALUES (:NEW.OCCNUM, vPORTENUM,'affecté');
        INSERT INTO AFFECTER_PK(OCCNUM,PKNUM,AFFPTKETAT) VALUES (:NEW.OCCNUM, vPKNUM,'affecté');
        dbms_output.put_line('IL N''YA PAS DE PASSERELLE AFFECTATION DU COUPLE (PORTENUM,PKNUM) ==> (' || vPORTENUM || ',' || vPKNUM || ')' );    
      WHEN OTHERS THEN 
        dbms_output.put_line('ERREUR DANS AFFECTATION DAND LE BLOC PASSERELLE  ==> ' || SQLERRM);   
     END;
   
  ELSE 
      --Dans le cas ou l'occurence n''a pas de couple porte/pk, on l'affecte alors ce couple par rapport au disponibilité
      p_affecte_porte_parking(:NEW.OCCNUM);        
  END IF;

EXCEPTION
  WHEN NO_DATA_FOUND THEN
      dbms_output.put_line('IL n'' ya pas d''occurence bidon correspondant pour cette occurence de vol');
  WHEN OTHERS THEN 
      dbms_output.put_line('ERREUR DANS DECL_INSERT_OCC_VOL  ==> ' || SQLERRM);   
END;
