BEGIN

BEGIN
  SELECT distinct pk.pknum INTO vPKNUM
    FROM occurence_vol ov1, vol v1, terminal t, desservir d, aeroport a, parking pk
    WHERE ov1.volnum = v1.volnum
    AND v1.aeronum_depart = t.aeronum
    AND ov1.occnum = pPKNUM
    AND t.ternum = d.ternum
    AND d.paysnum= a.paysnum
    AND v1.aeronum_arrivee=a.aeronum
    AND v1.aeronum_depart = pk.aeronum
    AND pk.pknum not in (select distinct pknum from passerelle)
    AND pk.pknum not in ( select ap.pknum
                          FROM affecter_pk ap, occurence_vol ov, vol v
                          WHERE ap.occnum=ov.occnum and ov.volnum=v.volnum and ov.occnum<>pPKNUM and ap.AFFPTKETAT='affecté'
                          AND to_char(ov.occdate+v.h_depart/24+ v.M_DEPART/(24*60)-v.nbminutesavant/(24*60),'dd/mm/yy hh24:mi:ss')
                          < to_char(ov1.occdate+v1.h_depart/24+ v1.M_DEPART/(24*60)+v1.nbminutesapres/(24*60),'dd/mm/yy hh24:mi:ss')
                          
                         AND to_char(ov.occdate+v.h_depart/24+v.M_DEPART/(24*60)-v.nbminutesavant/(24*60),'dd/mm/yy hh24:mi:ss')
                        > to_char(ov1.occdate+v1.h_depart/24+v1.M_DEPART/(24*60)-v1.nbminutesavant/(24*60),'dd/mm/yy hh24:mi:ss')
    
                        )
                        
    AND pk.pknum not in ( SELECT ap.pknum
                          FROM affecter_pk ap, occurence_vol ov, vol v
                          WHERE ap.occnum=ov.occnum and ov.volnum=v.volnum and ov.occnum<>pPKNUM and AFFPTKETAT='affecté'
                          AND to_char(ov.occdate+v.h_depart/24+ v.M_DEPART/(24*60)+v.nbminutesapres/(24*60),'dd/mm/yy hh24:mi:ss')
