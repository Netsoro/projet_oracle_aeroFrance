  CREATE OR REPLACE PROCEDURE "PROJET_ORACLE_DIAROG"."TESTSITOUSCOUPONSARRIVE" (pBILLNUM COUPON_VOL.BILLNUM%TYPE, vNB_OCC_VOL_ARRIVE OUT NUMBER,vNB_OCC_VOL_BILLET OUT NUMBER)  IS

BEGIN
  DBMS_OUTPUT.PUT_LINE('==========DEBUT testSiTousCouponsArrive==========');
  SELECT COUNT(OCCNUM) INTO vNB_OCC_VOL_ARRIVE FROM COUPON_VOL coup_v
  WHERE coup_v.BILLNUM = pBILLNUM AND COUPETAT= 'arrivé';
  
  SELECT COUNT(OCCNUM) INTO vNB_OCC_VOL_BILLET FROM COUPON_VOL coup_v
  WHERE coup_v.BILLNUM = pBILLNUM ;
  DBMS_OUTPUT.PUT_LINE('==========FIN testSiTousCouponsArrive==========');
END;

