       DBMS_OUTPUT.PUT_LINE('AUCUN PARKING DISPONIBLE');
       RETURN 0;
   WHEN others THEN
      DBMS_OUTPUT.PUT_LINE('ERREUR DANS F_GET_PARKING ==> ' || SQLERRM);
